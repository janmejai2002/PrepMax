describe('old check_in is permanently disabled', () => {
  it('junior calling check_in returns self_checkin_disabled', async () => {
    const { data, error } = await junior1Client.rpc('check_in', {
      p_slot_id: slotId,
      p_token:   'ANYTHING',
    })
    expect(error).toBeNull()
    expect(data.error).toBe('self_checkin_disabled')
  })

  it('host calling check_in also returns self_checkin_disabled', async () => {
    const { data } = await hostClient.rpc('check_in', {
      p_slot_id: slotId,
      p_token:   'ANYTHING',
    })
    expect(data.error).toBe('self_checkin_disabled')
  })
})

// ─────────────────────────────────────────────────────────────────────────────
describe('generate_checkin_token', () => {
  it('confirmed junior can generate a token for a live slot', async () => {
    const { data, error } = await junior1Client.rpc('generate_checkin_token', {
      p_slot_id: slotId,
    })
    expect(error).toBeNull()
    expect(data.error).toBeUndefined()
    expect(typeof data.token).toBe('string')
    // token = user_id:slot_id:exp:hmac — colon-separated 4 parts
    const parts = data.token.split(':')
    expect(parts).toHaveLength(4)
    expect(parts[0]).toBe(junior1Id)   // user_id is in the payload (not hidden)
    expect(parts[1]).toBe(slotId)
    expect(Number(parts[2])).toBeGreaterThan(Date.now() / 1000)
    junior1Token = data.token          // save for later fraud tests
  })

  it('unenrolled user cannot generate a token', async () => {
    const { data } = await outsiderClient.rpc('generate_checkin_token', {
      p_slot_id: slotId,
    })
    // 'not_enrolled' if authenticated but not enrolled; 'unauthorized' if session expired
    // Either way the user cannot get a token — both are correct rejections
    expect(['not_enrolled', 'unauthorized']).toContain(data.error)
  })

  it('token for a non-existent slot returns slot_not_found', async () => {
    const { data } = await junior1Client.rpc('generate_checkin_token', {
      p_slot_id: '00000000-0000-0000-0000-000000000000',
    })
    expect(data.error).toBe('slot_not_found')
  })
})

// ─────────────────────────────────────────────────────────────────────────────
describe('mark_attended_by_token — fraud paths', () => {
  it('FRAUD: junior cannot call mark_attended_by_token (unauthorized)', async () => {
    const { data } = await junior1Client.rpc('mark_attended_by_token', {
      p_slot_id: slotId,
      p_token:   junior1Token,
    })
    // junior1 is not the host/judge — must be rejected
    expect(data.error).toBe('unauthorized')
  })

  it('FRAUD: outsider cannot call mark_attended_by_token (unauthorized)', async () => {
    const { data } = await outsiderClient.rpc('mark_attended_by_token', {
      p_slot_id: slotId,
      p_token:   junior1Token,
    })
    expect(data.error).toBe('unauthorized')
  })

  it('FRAUD: forged token (tampered HMAC) is rejected', async () => {
    const parts = junior1Token.split(':')
    const forgedToken = parts[0] + ':' + parts[1] + ':' + parts[2] + ':' + 'deadbeef'.repeat(8)
    const { data } = await hostClient.rpc('mark_attended_by_token', {
      p_slot_id: slotId,
      p_token:   forgedToken,
    })
    expect(data.error).toBe('invalid_token_signature')
  })

  it('FRAUD: expired token (tampered exp in the past, original sig invalid) is rejected', async () => {
    const parts = junior1Token.split(':')
    // Change exp to Unix epoch 1 (1970-01-01); keep original sig → sig will be wrong,
    // but exp check fires first (exp=1 < now) and returns token_expired
    const expiredToken = parts[0] + ':' + parts[1] + ':1:' + parts[3]
    const { data } = await hostClient.rpc('mark_attended_by_token', {
      p_slot_id: slotId,
      p_token:   expiredToken,
    })
    expect(data.error).toBe('token_expired')
  })

  it('FRAUD: token for a different slot is rejected (slot mismatch)', async () => {
    const parts = junior1Token.split(':')
    const wrongSlot = '11111111-1111-1111-1111-111111111111'
    // Swap slot_id in payload; sig is now wrong too, but slot mismatch fires first
    const mismatchedToken = parts[0] + ':' + wrongSlot + ':' + parts[2] + ':' + parts[3]
    const { data } = await hostClient.rpc('mark_attended_by_token', {
      p_slot_id: slotId,
      p_token:   mismatchedToken,
    })
    // Token slot_id !== p_slot_id
    expect(data.error).toBe('token_slot_mismatch')
  })

  it('host marks junior1 attended with valid token → success', async () => {
    const { data, error } = await hostClient.rpc('mark_attended_by_token', {
      p_slot_id: slotId,
      p_token:   junior1Token,
    })
    expect(error).toBeNull()
    expect(data.status).toBe('attended')
    expect(data.user_id).toBe(junior1Id)

    const { data: enroll } = await adminSb
      .from('enrollments')
      .select('status, attended_at, checked_in_by')
      .eq('slot_id', slotId).eq('user_id', junior1Id).single()
    expect(enroll!.status).toBe('attended')
    expect(enroll!.attended_at).toBeTruthy()
    expect(enroll!.checked_in_by).toBe(hostId)   // scanner identity recorded
  })

  it('FRAUD: token reuse after successful check-in returns token_already_used', async () => {
    const { data } = await hostClient.rpc('mark_attended_by_token', {
      p_slot_id: slotId,
      p_token:   junior1Token,
    })
    expect(data.error).toBe('token_already_used')
  })

  it('co-judge can also mark attendance via token (generates junior2 token)', async () => {
    const { data: tokenData } = await junior2Client.rpc('generate_checkin_token', {
      p_slot_id: slotId,
    })
    expect(tokenData.error).toBeUndefined()

    const { data } = await judgeClient.rpc('mark_attended_by_token', {
      p_slot_id: slotId,
      p_token:   tokenData.token,
    })
    expect(data.status).toBe('attended')
    expect(data.user_id).toBe(junior2Id)

    const { data: enroll } = await adminSb
      .from('enrollments')
      .select('checked_in_by')
      .eq('slot_id', slotId).eq('user_id', junior2Id).single()
    expect(enroll!.checked_in_by).toBe(judgeId)  // judge's identity recorded
  })
})

