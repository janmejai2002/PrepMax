/**
 * Attendance fraud-path tests.
 *
 * Every test here asserts that a fraudulent check-in attempt FAILS.
 * The new secure model: junior shows personal QR → HOST scans and marks attendance.
 * Junior cannot self-mark; tokens are HMAC-signed, expiring, and single-use.
 */
import { beforeAll, afterAll, describe, it, expect } from 'vitest'
import { createClient, SupabaseClient } from '@supabase/supabase-js'
import * as dotenv from 'dotenv'
import { resolve } from 'path'

dotenv.config({ path: resolve(process.cwd(), '.env.local') })

const URL  = process.env.NEXT_PUBLIC_SUPABASE_URL!
const ANON = process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!
const SVC  = process.env.SUPABASE_SERVICE_ROLE_KEY!

const HOST_EMAIL     = 'fraud-host@prepmax-test.local'
const JUDGE_EMAIL    = 'fraud-judge@prepmax-test.local'
const JUNIOR1_EMAIL  = 'fraud-junior1@prepmax-test.local'
const JUNIOR2_EMAIL  = 'fraud-junior2@prepmax-test.local'
const JUNIOR3_EMAIL  = 'fraud-junior3@prepmax-test.local'
const OUTSIDER_EMAIL = 'fraud-outsider@prepmax-test.local'
const PASSWORD       = 'fraud-test-pass-2026!'

let adminSb:        SupabaseClient
let hostClient:     SupabaseClient
let judgeClient:    SupabaseClient
let junior1Client:  SupabaseClient
let junior2Client:  SupabaseClient
let junior3Client:  SupabaseClient
let outsiderClient: SupabaseClient

let hostId:    string
let judgeId:   string
let junior1Id: string
let junior2Id: string
let junior3Id: string
let roomId:    string
let slotId:    string
let slot2Id:   string  // separate slot for mark_attended_direct tests (fresh confirmed seats)

// token generated by junior1 for use across tests
let junior1Token: string

async function makeUser(email: string, canHost = false): Promise<string> {
  const { data: existing } = await adminSb.auth.admin.listUsers({ perPage: 1000 })
  const found = existing?.users?.find((u) => u.email === email)
  if (found) {
    await adminSb.auth.admin.updateUserById(found.id, { password: PASSWORD })
    return found.id
  }
  const { data } = await adminSb.auth.admin.createUser({
    email, password: PASSWORD, email_confirm: true,
  })
  const uid = data.user!.id
  await adminSb.from('profiles').upsert({
    id: uid, email, name: email.split('@')[0],
    year: canHost ? 'second' : 'first',
    can_host_gd: canHost, can_host_pi: canHost,
    is_crisp: false, is_sac: false,
  }, { onConflict: 'id' })
  return uid
}

async function loginClient(email: string): Promise<SupabaseClient> {
  const c = createClient(URL, ANON, { auth: { autoRefreshToken: false, persistSession: false } })
  await c.auth.signInWithPassword({ email, password: PASSWORD })
  return c
}

beforeAll(async () => {
  if (!SVC) throw new Error('SUPABASE_SERVICE_ROLE_KEY not set')
  adminSb = createClient(URL, SVC, { auth: { autoRefreshToken: false, persistSession: false } })

  hostId    = await makeUser(HOST_EMAIL, true)
  judgeId   = await makeUser(JUDGE_EMAIL, true)
  junior1Id = await makeUser(JUNIOR1_EMAIL, false)
  junior2Id = await makeUser(JUNIOR2_EMAIL, false)
  junior3Id = await makeUser(JUNIOR3_EMAIL, false)
  await makeUser(OUTSIDER_EMAIL, false)

  hostClient     = await loginClient(HOST_EMAIL)
  judgeClient    = await loginClient(JUDGE_EMAIL)
  junior1Client  = await loginClient(JUNIOR1_EMAIL)
  junior2Client  = await loginClient(JUNIOR2_EMAIL)
  junior3Client  = await loginClient(JUNIOR3_EMAIL)
  outsiderClient = await loginClient(OUTSIDER_EMAIL)

  const { data: room, error: roomErr } = await adminSb.from('rooms').insert({
    name: 'Fraud Test Room', location: 'Test Block', capacity: 20, is_live: true,
  }).select('id').single()
  if (roomErr || !room) throw new Error(`Room insert failed: ${JSON.stringify(roomErr)}`)
  roomId = room.id

  const start = new Date(Date.now() + 60_000)
  const end   = new Date(Date.now() + 3_660_000)

  // slotId: for mark_attended_by_token tests (junior1 + junior2 enroll here)
  const { data: slot } = await adminSb.from('slots').insert({
    type: 'GD', host_id: hostId, room_id: roomId,
    topic: 'Fraud Test GD', capacity: 4, expert_areas: [],
    start_at: start.toISOString(), end_at: end.toISOString(),
  }).select('id').single()
  slotId = slot!.id
  await adminSb.from('slot_judges').insert({ slot_id: slotId, judge_id: judgeId })
  await junior1Client.rpc('join_slot', { p_slot_id: slotId, p_user_id: junior1Id })
  await junior2Client.rpc('join_slot', { p_slot_id: slotId, p_user_id: junior2Id })
  await hostClient.rpc('start_slot', { p_slot_id: slotId })

  // slot2Id: for mark_attended_direct tests (junior3 enrolls here — fresh confirmed seat)
  const { data: slot2 } = await adminSb.from('slots').insert({
    type: 'GD', host_id: hostId, room_id: roomId,
    topic: 'Direct Mark Test', capacity: 4, expert_areas: [],
    start_at: start.toISOString(), end_at: end.toISOString(),
  }).select('id').single()
  slot2Id = slot2!.id
  await junior3Client.rpc('join_slot', { p_slot_id: slot2Id, p_user_id: junior3Id })
  await hostClient.rpc('start_slot', { p_slot_id: slot2Id })
}, 90_000)

afterAll(async () => {
  await adminSb.from('slots').delete().eq('id', slotId)
  await adminSb.from('slots').delete().eq('id', slot2Id)
  await adminSb.from('rooms').delete().eq('id', roomId)
  const allEmails = [HOST_EMAIL, JUDGE_EMAIL, JUNIOR1_EMAIL, JUNIOR2_EMAIL, JUNIOR3_EMAIL, OUTSIDER_EMAIL]
  const { data: list } = await adminSb.auth.admin.listUsers({ perPage: 1000 })
  for (const email of allEmails) {
    const u = list?.users?.find((x) => x.email === email)
    if (u) await adminSb.auth.admin.deleteUser(u.id)
  }
})

